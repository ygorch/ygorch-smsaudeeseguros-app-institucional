-- RLS Policies
-- Profiles
CREATE POLICY "Public profiles are viewable by everyone" ON profiles FOR SELECT USING (true);
CREATE POLICY "Users can insert their own profile" ON profiles FOR INSERT WITH CHECK (auth.uid() = id);
-- Note: 'admin' check relies on the user already being an admin, bootstrapping problem solved by manual insert or service role
CREATE POLICY "Admins can update all profiles" ON profiles FOR UPDATE USING (
  exists (select 1 from profiles where id = auth.uid() and role = 'admin')
);

-- Site Config & Sections: Read Public, Write Authenticated (Assuming any auth user is staff for now, since signup is closed)
-- Actually, strict RBAC would be better, but "auth.role() = 'authenticated'" is good for now as per requirement
-- "não é permitido adicionar novos usuarios, apenas administradores" -> so only staff exists.
CREATE POLICY "Read access for all" ON site_config FOR SELECT USING (true);
CREATE POLICY "Write access for authenticated users" ON site_config FOR UPDATE USING (auth.role() = 'authenticated');

CREATE POLICY "Read access for all" ON hero_section FOR SELECT USING (true);
CREATE POLICY "Write access for authenticated users" ON hero_section FOR UPDATE USING (auth.role() = 'authenticated');

CREATE POLICY "Read access for all" ON about_section FOR SELECT USING (true);
CREATE POLICY "Write access for authenticated users" ON about_section FOR UPDATE USING (auth.role() = 'authenticated');

CREATE POLICY "Read access for all" ON about_spotlights FOR SELECT USING (true);
CREATE POLICY "Write access for authenticated users" ON about_spotlights FOR ALL USING (auth.role() = 'authenticated');

CREATE POLICY "Read access for all" ON about_images FOR SELECT USING (true);
CREATE POLICY "Write access for authenticated users" ON about_images FOR ALL USING (auth.role() = 'authenticated');

CREATE POLICY "Read access for all" ON solutions FOR SELECT USING (true);
CREATE POLICY "Write access for authenticated users" ON solutions FOR ALL USING (auth.role() = 'authenticated');

CREATE POLICY "Read access for all" ON testimonials FOR SELECT USING (true);
CREATE POLICY "Write access for authenticated users" ON testimonials FOR ALL USING (auth.role() = 'authenticated');

CREATE POLICY "Read access for all" ON faqs FOR SELECT USING (true);
