  seo_description TEXT DEFAULT 'Consultoria especializada em planos de saúde e seguros. Proteja o que mais importa com a SM Saúde e Seguros.',
  og_image_url TEXT,
  contact_email TEXT DEFAULT 'contato@smsaudeseguros.com.br',
  contact_phone TEXT DEFAULT '(15) 99999-9999',
  contact_address TEXT DEFAULT 'Sorocaba, SP (Atendimento Nacional)',
  contact_whatsapp TEXT DEFAULT '5515999999999',
  social_instagram TEXT DEFAULT 'https://instagram.com',
  social_facebook TEXT DEFAULT 'https://facebook.com',
  social_linkedin TEXT DEFAULT 'https://linkedin.com',
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

