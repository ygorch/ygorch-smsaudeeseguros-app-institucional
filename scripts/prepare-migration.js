const { createClient } = require('@supabase/supabase-js');

const supabaseUrl = process.env.SUPABASE_URL;
const supabaseServiceKey = process.env.SUPABASE_SECRET_SERVICE_ROLE;

if (!supabaseUrl || !supabaseServiceKey) {
  console.error('Missing SUPABASE_URL or SUPABASE_SECRET_SERVICE_ROLE');
  process.exit(1);
}

const supabase = createClient(supabaseUrl, supabaseServiceKey);

async function main() {
  const sql = `
    -- Create lead_interactions table
    CREATE TABLE IF NOT EXISTS lead_interactions (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      lead_id BIGINT REFERENCES leads(id) ON DELETE CASCADE,
      user_id UUID REFERENCES auth.users(id) ON DELETE SET NULL,
      interaction_type TEXT NOT NULL CHECK (interaction_type IN ('email', 'phone', 'whatsapp', 'personally')),
      notes TEXT,
      created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
    );

    -- Enable RLS
    ALTER TABLE lead_interactions ENABLE ROW LEVEL SECURITY;

    -- Create policies
    DO $$ BEGIN
      CREATE POLICY "Authenticated users can view interactions" ON lead_interactions
        FOR SELECT USING (auth.role() = 'authenticated');
    EXCEPTION
      WHEN duplicate_object THEN null;
    END $$;

    DO $$ BEGIN
      CREATE POLICY "Authenticated users can insert interactions" ON lead_interactions
        FOR INSERT WITH CHECK (auth.role() = 'authenticated');
    EXCEPTION
      WHEN duplicate_object THEN null;
    END $$;
  `;

  const { error } = await supabase.rpc('exec_sql', { sql_query: sql }).catch(async () => {
      // Fallback: If exec_sql RPC doesn't exist (it usually doesn't by default), we can't run DDL easily via JS client
      // without the service role key AND direct SQL access or a helper function.
      // However, Supabase JS client generally doesn't support raw SQL execution directly on the client instance
      // unless a specific RPC is set up.

      // Since I don't have the password to connect via psql/pg, and I might not have an RPC...
      // I will try to use the 'postgres' package if available or just output the SQL for the user?
      // Wait, I can use the 'admin' API features or just creating a migration file might be safer.
      // BUT, the instructions say "Run the insertion script".

      // Let's try to assume the user wants me to create the file `supabase/migrations/timestamp_lead_interactions.sql`
      // and they might run it? No, the toolset allows me to *create* the table if I had access.

      // Actually, checking previous turns, I used `supabase.from(...).insert(...)` which worked.
      // But creating a TABLE via JS client isn't standard.

      // Alternative: I will create a Migration SQL file and then TRY to execute it if I can finding a way,
      // otherwise I will instruct the user or use a workaround.
      // Wait, do I have `pg` installed?
      return { error: 'Direct SQL execution not supported via standard client without RPC.' };
  });

  // Re-evaluating: I can't run DDL (CREATE TABLE) via supabase-js client directly.
  // I should check if there is a 'postgres' library installed or if I can install it.

  console.log("Migration SQL prepared. Since direct DDL via JS client is restricted, I will use 'pg' via npx or similar if possible.");
}

